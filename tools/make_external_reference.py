# ==================================================================
#  Conversions -- printed angles/dates to the library's integer units
# ==================================================================

def ut1_ms(Y, M, D, h, m=0, s=0):
    """Calendar UT (treated as UT1) to int64 ms since J2000.0."""
    return round((datetime(Y, M, D, h, m, s) - J2000).total_seconds() * 1000)


def parse_date(s):
    """'2025-01-04 05:00' or '...:00' -> (Y,M,D,h,m,s)."""
    d, t = s.split()
    Y, M, D = (int(x) for x in d.split("-"))
    parts = [int(x) for x in t.split(":")]
    while len(parts) < 3:
        parts.append(0)
    return Y, M, D, parts[0], parts[1], parts[2]


def leap_seconds(Y, M):
    """TAI - UTC (integer leap seconds) in effect at year Y month M,
    for the broad-era rows that carry no printed dTT. IERS history."""
    ym = Y * 12 + (M - 1)
    if ym >= 2017 * 12 + 0:  return 37   # 2017-01-01
    if ym >= 2015 * 12 + 6:  return 36   # 2015-07-01
    if ym >= 2012 * 12 + 6:  return 35   # 2012-07-01
    if ym >= 2009 * 12 + 0:  return 34   # 2009-01-01
    if ym >= 2006 * 12 + 0:  return 33   # 2006-01-01
    return 32                            # 1999-01-01 .. 2005-12-31


def tt_minus_ut1_ms_era(Y, M):
    """TT - UT1 = 32.184 s + (TAI-UTC) - DUT1, DUT1 ~ 0 (|DUT1| < 0.9 s,
    below the Sun's TT sensitivity here)."""
    return 32184 + 1000 * leap_seconds(Y, M)


def gha_dec_to_q30(gha_deg, gha_min, dec_deg, dec_min):
    """Printed GHA/dec to an earth-fixed Q2.30 unit vector, the same
    convention astro_nav_celestial_to_earthfixed() emits: x at
    (lat 0, lon 0), y at (lat 0, lon 90 E), z at the north pole, with
    east-longitude = -GHA and latitude = declination. dec_deg carries
    the sign; dec_min is its magnitude -- except a 0-degree southern
    dec, written (0, -m) with the sign on the minutes, which the sum
    below folds in correctly since degree 0 contributes no sign."""
    gha = math.radians(gha_deg + gha_min / 60.0)
    sign = -1.0 if dec_deg < 0 else 1.0
    dec = math.radians(sign * (abs(dec_deg) + dec_min / 60.0))
    lon = -gha
    x = math.cos(dec) * math.cos(lon)
    y = math.cos(dec) * math.sin(lon)
    z = math.sin(dec)
    return round(x * Q30), round(y * Q30), round(z * Q30)


def gha_marcmin(gha_deg, gha_min):
    """GHA in milli-arcminutes, normalized to [0, 21600000)."""
    v = round((gha_deg * 60 + gha_min) * 1000)
    return v % 21600000


