#!/usr/bin/env python3
"""Generate external_reference.h: truth rows from PRINTED / PUBLISHED
sources, independent of both this library's formulas AND of Skyfield.

--ephemeris-check already measures the almanac against Skyfield/DE421.
This second gate, --external-check, measures it against numbers a human
read off a printed page: daily-page Sun and Moon GHA/dec (plus hourly
Moon HP) and GHA Aries hours, an independent broad-era Sun/Moon/star
table, the Nautical Almanac refraction and dip tables, and one Bowditch
worked correction example. Two independent authorities disagreeing with
us the same way is much stronger evidence than either alone.

This is a one-time OFFLINE transcriber: the numeric facts below were
read from published almanacs (and public test suites that quote them);
this script converts dates to UT1 milliseconds and angles to the
library's integer units and emits external_reference.h. The repo
commits the integer output and never runs Python at build or test
time. Deterministic: re-running reproduces the header byte-identical.

  python3 tools/make_external_reference.py > external_reference.h

Provenance of the transcribed numbers (publications, not code). Each
collecting repo is pinned to the exact revision the numbers were read
from, so the transcription is auditable against a fixed source tree:
  * Paracay Nautical Almanac 2021; NA 2002; USNO Air Almanac 2023;
    EZ Celestial Almanac 2023 -- daily-page Sun and Moon GHA/dec (and
    hourly Moon HP) and GHA Aries hourly rows, hand-transcribed from
    the printed pages (collected via
    github.com/quantenschaum/nautical_almanac, its daily-pages-*.txt,
    revision 799a9c9667bad428f450a8559063010cbabc871e).
  * Nautical Almanac Sun (GHA/dec/SD), Moon (GHA/dec/HP/SD) and star
    (GHA/dec) rows, 2001-2025, tabulated in the OpenCPN
    celestial_navigation_pi test suite
    (github.com/rgleason/celestial_navigation_pi, test/altitude_tests.cpp,
    SUNMOON_SIGHTS / STAR_SIGHTS -- the "Nautical Almanac" reference column,
    revision e442c28ae0aeea98429391a143592c4c96f1d3a5).
  * Nautical Almanac standard refraction table (P=1010 mb, T=10 C) and
    dip table, transcribed via github.com/alejandrozarco/celestial-navigator
    (revision c09c04dcb950ed1ae27f08dd1c2992c48b85f990).
  * Additional Nautical Almanac GHA-Aries values (near the 0/180/360
    wrap) quoted in github.com/DaniilGalahov/celnav test_almanac.py
    (revision 54e81da6fd29c7c8fdd16e410a00c5747e624dde).
  * Bowditch, American Practical Navigator, vol. 1 (2019), altitude-
    correction worked example (p. 315), via github.com/DaniilGalahov/celnav
    (same revision).

TIME POLICY: almanac daily pages tabulate against UT, and this library's
rotation runs on UT1, so we treat each tabulated page hour as UT1
directly. If a publisher actually meant UTC, the error is at most |DUT1|
< 0.9 s of earth rotation = 0.9 s x 15.041 "/s ~ 0.23' of GHA -- inside
every tolerance measured below. Each daily-page block also carries the
page's printed dTT = TT - UT1, used for the Sun's dynamics; the broad-era
rows (no printed dTT) use a per-era leap-second approximation, and the
Sun's <=0.05'/several-seconds sensitivity to TT makes the residual
negligible against the ~1' Sun tolerance. The Moon moves ~0.55'/min
against the stars, so its dynamics DO need real TT -- the pages' printed
dTT provides it, and the broad-era approximation is wrong by at most
|DUT1| < 0.9 s, under 0.01' of Moon motion.
"""

